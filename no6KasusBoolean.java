public class no6KasusBoolean {
    /**
     * @param args
     */
    public static void main(String[] args) {
        // Kamus
        boolean bool;

        // Algoritma
        bool = true; 

        // Mengecek nilai boolean
        if (bool) {
            System.out.print("true\n"); 
        } else {
            System.out.print("false\n"); 
        }

        // Mengecek negasi dari boolean
        if (!bool) {
            System.out.print("salah\n"); 
        } else {
            System.out.print("benar\n"); 
        }
    }
}